""" TIPOS SIMPLES """

a = 5
print(type(a))

b = 3.14
print(type(b))

c = "Cadena"
d = 'Hola que tal'

print(type(c))
print(type(d))

# LISTA - puede cambiar
e=[1,"hola",[3,6],7.8]
print(type(e))

# TUPLA - inmutable
f = (1,"hola",[3,4,7.8])
print(type(f))

# DICCIONARIO
g = {'pi':3.14,'e':2.71}
print(type(g))
 
